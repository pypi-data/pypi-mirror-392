def invert_lightness( colors ):
    shape = colors.shape
    C = colors.reshape((-1,shape[-1]))
    R = np.copy(C[:,0])
    G = np.copy(C[:,1])
    B = np.copy(C[:,2])
    A = np.copy(C[:,3])
    final = [1.-0.5*(G+B), 1.-0.5*(R+B), 1.-0.5*(R+G), A]
    return np.array(final).T.reshape(shape)

def stretch_center( cmap, fac, use_hyper=False, inv_l=False, endpoint=1.0, compress=False ):
    funs = (np.arctan, np.tan)
    if use_hyper:
        funs = (np.tanh, np.arctanh)
    if compress:
        funs = (funs[1], funs[0])
    xm = funs[0](fac)
    ary = (funs[1]( np.linspace(-xm*endpoint, xm*endpoint, 1024) ) + fac)/(2*fac)
    colors = cmap(ary)
    if inv_l:
        colors = invert_lightness(colors)
    return mpl_c.LinearSegmentedColormap.from_list( 'CmapCustom', colors )

def cut_cmap( cmap, start=0.0, end=1.0, invert=False ):
    x = np.linspace(start, end, 1024)
    if invert:
        x = x[::-1]
    colors = cmap(x)
    return mpl_c.LinearSegmentedColormap.from_list( 'CmapCustom', colors )

