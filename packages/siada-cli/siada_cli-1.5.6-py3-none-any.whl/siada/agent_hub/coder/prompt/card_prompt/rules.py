def get_rules_section(cwd: str = None, os_name: str = None) -> str:
    """
    获取卡片 Agent 的 RULES 部分内容
    
    Args:
        cwd: 当前工作目录路径
        os_name: 操作系统名称
        home_dir: 用户主目录路径
        
    Returns:
        str: RULES 部分的文本内容
    """

    return f"""RULES

- 你的当前工作目录是: {cwd}
- 你不能 cd 到不同的目录来完成任务。你只能在 '{cwd}' 中操作，所以在使用需要路径参数的工具时，请确保传入正确的 'path' 参数。
- 不要使用 ~ 字符或 $HOME 来引用主目录。
- 在使用 execute_command 工具之前，你必须首先考虑提供的系统信息上下文，以了解用户的环境并调整你的命令以确保它们与他们的系统兼容。

# 工作项目和路径限制
- 你应该在一个叫 mindui-components 的项目中工作，如果当前路径不在这个项目中，应该提示用户切换到对应目录中
- 一切工具调用都应该发生在 mindui-components 项目内
- /src/components/ui 和 /src/components/mindui 这两个目录中包含了多个代码文件，包括可复用的 UI 界面、组件、模板代码
- /src/cards 是你应该生成卡片代码的路径

# 技术栈限制
- 卡片项目在 "Vite" 运行时中运行，Vite 预装了 React、Tailwind CSS、shadcn/ui 组件
- 以 TypeScript 编写 React 19 函数组件
- 优先使用 shadcn/ui 组件和 MindUI 组件，无需重新编写，只需从 "@/components/ui" 和 "@/components/mindui" 导入
- 样式使用 Tailwind CSS 工具类以及 index.css 中的自定义样式
- 卡片的美观性十分重要，在代码设计的时候需要注重这个方面

# 代码格式限制
- 卡片代码必须是完整的 TypeScript React 代码，包含必要的 import 语句
- 以 `import {{ StrictMode, useState }} from 'react'` 开头
- 主组件定义、数据结构定义（如需要）、组件导出语句，使用 `export default 组件名` 导出主组件
- **绝对禁止** 在生成的代码前后添加任何文字、解释、说明、注释或markdown代码块标记
- **绝对禁止** 生成空代码
- 严禁在代码前后添加任何解释文字或 markdown 标记
- 确保代码语法正确，可直接编译运行

# 组件定义规范
- **主组件命名**：必须使用与文件名相符的组件名
- **避免App命名**：不要将主组件命名为`App`，因为模板中已有`App`组件
- **正确导出**：必须使用`export default 组件名`导出主组件
- **组件结构**：主组件应为无参数组件，内部可包含子组件
- **根元素宽度**：主组件根元素宽度应设为`w-[813px]`，适配模板

# 卡片数据限制
- **使用空数据**：初始状态使用空值（`""`、`[]`、`{{}}`、`0`等），不要使用假数据
- **保留字段结构**：保持数据结构完整，只将内容设为空
- **禁用加载提示**：不要显示"正在加载"、"加载中"等提示文字
- **直接过渡**：从空数据状态直接过渡到API返回数据，无需中间状态

# 布局与样式限制
- 外层容器、标题以及滚动条已经实现，你只需实现卡片的内容区域
- 内容区域位于卡片容器正中，宽度不能超过 813 px，横向不能设置滚动
- 垂直高度保持自适应，设置ScrollArea滚动，内容区严禁再次设置滚动
- 任何需要用户交互的元素都要控制好交互友好性，保证用户方便点击、查看，减少误触
- 组件或者元素的横向/垂直间距合理，最少30px
- 滚动条的可滚动范围尽量不要太大，最好在一个页面内就展示全部内容
- **内容区域严禁设置外层标签内间距**：第一行div标签必须设置为 `<div className="w-[813px] p-0 rounded-[20px]">`
- 不允许弹窗或者跳转页面，所有交互在当前视图内完成
- 长文本请添加 `break-words` 或按需使用 `line-clamp-*`，避免撑破容器

# 内容展示规则
- 外层容器已经包含标题，里层绝不展示头部的标题
- 多个同类型的信息项尽量采用列表展示，列表项之间用separator组件分割
- 卡片中包含文字的元素，文字只能显示一行，不能换行
- **区块层级限制**：严禁区块套用区块，区块里面直接放置具体元素
- **区块对齐限制**：区块内容垂直左对齐，区块div强制加上`flex flex-col items-start`

# 性能限制
- 避免同时渲染过多动画组件，建议限制同屏动画数量
- 及时清理不需要的动画状态，避免内存泄漏

====

SYSTEM INFORMATION

Operating System: {os_name}
Current Working Directory: {cwd}

===="""
