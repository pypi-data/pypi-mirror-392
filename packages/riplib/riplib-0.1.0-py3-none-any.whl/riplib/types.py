from enum import Enum

class Smell(Enum):
    """
    Represents the smell of a fart.
    """
    PUTRID = "putrid"
    FOUL = "foul"
    NEUTRAL = "neutral"
    PLEASANT = "pleasant"
    HEAVENLY = "heavenly"

class Power(Enum):
    """
    Represents the power of a fart.
    """
    WEAK = "weak"
    MODERATE = "moderate"
    STRONG = "strong"
    EXPLOSIVE = "explosive"

class Persistence(Enum):
    """
    Represents the persistence of a fart.
    """
    FLEETING = "fleeting"
    SHORT = "short"
    MEDIUM = "medium"
    LONG = "long"
    ETERNAL = "eternal"

class Soup(Enum):
    """
    Represents the soupiness of a fart.
    """
    NONE = "none"
    MINOR = "minor"
    MAJOR = "major"
    CATASTROPHIC = "catastrophic"